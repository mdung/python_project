def add_numbers(a, b):
    """
    Add two numbers.

    Parameters:
    - a (int): The first number.
    - b (int): The second number.

    Returns:
    int: The sum of the two numbers.
    """
    return a + b

if __name__ == "__main__":
    result = add_numbers(5, 7)
    print(f"The result is: {result}")
